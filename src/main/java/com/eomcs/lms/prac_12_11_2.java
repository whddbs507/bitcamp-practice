package com.eomcs.lms;

public class prac_12_11_2 {

  public static void main(String[] args) {
      int i;   // int : 데이터 타입, i : 변수(variable)

      //// 정수 ////
      byte a;   // byte = 1 byte    (-128 ~ 127)
      short b;  // short = 2 byte   (-32768 ~ 32767)
      int c;    // int = 4 byte     (약 -21억 ~ 21억)
      long d;   // long = 8 byte    (약 -922경 ~ 922경)

      //// 부동소수 ////
      float e;  // float = 4 byte   (유효자릿수 7자리)
      double f; // double = 8 byte  (유효자릿수 15자리)

      ///// 문자 ////
      char g;   // char = 2 byte;   (0 ~ 65535). UCS-2 코드 값 저장.
  }
}